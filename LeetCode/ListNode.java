// Definition for singly-linked list.
public class ListNode {
    public int val;
    public ListNode next;

    // Constructor
    public ListNode(int val) {
        this.val = val;
        this.next = null;
    }

    // Optional: constructor with next node
    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
